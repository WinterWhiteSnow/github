class 슉:
    def __init__(self):
        self.result = "슉"
    def 슈(self):
        self.result += ".슈"
    def 슉(self):
        self.result += "슉"
    def 슈슉(self):
        self.result += ".슈슉"
    def 시(self):
        self.result += ".시"
    def 시발(self):
        self.result += ".시발"
    def 시발럼아(self):   
        self.result += ".시발럼아"
    def ret(self):
        return self.result

슉 = 슉()
슉.슈()
슉.슈슉()
슉.시()
슉.시발()
슉.시발럼아()
print(슉.ret())
